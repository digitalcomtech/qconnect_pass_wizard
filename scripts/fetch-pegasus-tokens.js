#!/usr/bin/env node
/**
 * Fetch fresh Pegasus auth tokens and write QA_* env vars to .env.local (gitignored).
 *
 * Credentials MUST come from the environment — never pass passwords on the command line.
 *
 *   export PEGASUS_AUTH_USERNAME='you@company.com'
 *   export PEGASUS_AUTH_PASSWORD='your-password'
 *   npm run pegasus:fetch-tokens
 *
 * Gateways (override if needed):
 *   PEGASUS_AUTH_GATEWAY_PEGASUS1=cloud.pegasusgateway.com
 *   PEGASUS_AUTH_GATEWAY_PEGASUS256=qconnect.qualitas.com.mx
 *   PEGASUS_AUTH_GATEWAY_QSERVICES=dev2.pegasusgateway.com  (QA qservices Bearer; optional override)
 */

'use strict';

const fs = require('fs');
const path = require('path');
const { fetchPegasusAuthToken } = require('../services/pegasus/auth-token');

const OUT = path.join(process.cwd(), '.env.local');
const PREFIX = process.env.PEGASUS_TOKEN_ENV_PREFIX || 'QA';

async function main() {
  const username = process.env.PEGASUS_AUTH_USERNAME;
  const password = process.env.PEGASUS_AUTH_PASSWORD;

  if (!username || !password) {
    console.error(
      'Set PEGASUS_AUTH_USERNAME and PEGASUS_AUTH_PASSWORD, then run: npm run pegasus:fetch-tokens'
    );
    process.exit(1);
  }

  const gateway1 =
    process.env.PEGASUS_AUTH_GATEWAY_PEGASUS1 || 'cloud.pegasusgateway.com';
  const gateway256 =
    process.env.PEGASUS_AUTH_GATEWAY_PEGASUS256 || 'qconnect.qualitas.com.mx';
  const gatewayQ =
    process.env.PEGASUS_AUTH_GATEWAY_QSERVICES ||
    (PREFIX === 'QA' ? 'dev2.pegasusgateway.com' : '');

  console.log('Fetching Pegasus1 token (gateway:', gateway1, ')…');
  const p1 = await fetchPegasusAuthToken({
    username,
    password,
    gateway: gateway1,
  });
  console.log(' ', p1.message);

  console.log('Fetching Pegasus256 token (gateway:', gateway256, ')…');
  const p256 = await fetchPegasusAuthToken({
    username,
    password,
    gateway: gateway256,
  });
  console.log(' ', p256.message);

  const lines = [
    '# Auto-generated by scripts/fetch-pegasus-tokens.js — do not commit',
    `# ${new Date().toISOString()}`,
    `ENVIRONMENT=qa`,
  ];
  if (PREFIX === 'QA') {
    lines.push('QA_PEGASUS_BASE_URL=https://qservices.pegasusgateway.com/qa');
  }
  lines.push(
    `${PREFIX}_PEGASUS1_TOKEN=${p1.token}`,
    `${PREFIX}_PEGASUS256_TOKEN=${p256.token}`,
    '',
    '# api.pegasusgateway.com (devices, groups, vehicles) — uses Pegasus1 token when main token unset',
    `# ${PREFIX}_PEGASUS_TOKEN=`,
    '',
    '# qservices installations (Bearer) — auth gateway dev2.pegasusgateway.com when PREFIX=QA',
  );

  if (gatewayQ) {
    console.log('Fetching qservices token (gateway:', gatewayQ, ')…');
    try {
      const pq = await fetchPegasusAuthToken({
        username,
        password,
        gateway: gatewayQ,
      });
      console.log(' ', pq.message);
      lines.push(`${PREFIX}_PEGASUS_TOKEN=${pq.token}`);
      console.log(
        `  → set ${PREFIX}_PEGASUS_TOKEN and ${PREFIX}_PEGASUS_BASE_URL in .env.local; installation search uses Bearer on that host.`
      );
    } catch (err) {
      console.warn('  qservices gateway failed:', err.message);
      lines.push(`# ${PREFIX}_PEGASUS_TOKEN=  # failed for gateway ${gatewayQ}`);
    }
  } else {
    lines.push(
      `# ${PREFIX}_PEGASUS_TOKEN=  # add PEGASUS_AUTH_GATEWAY_QSERVICES and re-run if you have a qservices gateway`
    );
  }

  fs.writeFileSync(OUT, lines.join('\n') + '\n', 'utf8');
  console.log('');
  console.log('Wrote', OUT);
  console.log('Restart the server so it loads these tokens, e.g.:');
  console.log('  npm run pegasus:refresh-and-start');
  console.log('Or: set -a && source .env.local && set +a && npm start');
}

if (require.main === module) {
  main().catch((e) => {
    console.error('fetch-pegasus-tokens failed:', e.message);
    process.exit(1);
  });
}

module.exports = { main };
